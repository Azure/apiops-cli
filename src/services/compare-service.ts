/**
 * Compares two resource lists and returns differences
 */
function compareResourceLists(
  typeLabel: string,
  sourceResources: Array<Record<string, unknown>>,
  targetResources: Array<Record<string, unknown>>,
  normalizeContext: NormalizeContext,
  excludeNames?: Set<string>,
  skipSecretValues = false,
  skipLoggerCreds = false,
): ComparisonDifference[] {
  const differences: ComparisonDifference[] = [];

  // Build resource maps, handling auto-generated IDs
  const sourceMap = buildResourceMap(
    sourceResources,
    normalizeContext,
    excludeNames,
  );
  const targetMap = buildResourceMap(
    targetResources,
    normalizeContext,
    excludeNames,
  );

  // Find missing resources (in source but not in target)
  for (const name of sourceMap.keys()) {
    if (!targetMap.has(name)) {
      differences.push({
        resourceType: typeLabel,
        resourceName: name,
        diffType: 'missing',
      });
    }
  }

  // Find extra resources (in target but not in source)
  for (const name of targetMap.keys()) {
    if (!sourceMap.has(name)) {
      differences.push({
        resourceType: typeLabel,
        resourceName: name,
        diffType: 'extra',
      });
    }
  }

  // Compare matched resources
  for (const name of sourceMap.keys()) {
    if (!targetMap.has(name)) continue;

    const sourceResource = sourceMap.get(name)!;
    const targetResource = targetMap.get(name)!;

    const sourceNorm = normalizeResource(sourceResource, normalizeContext);
    const targetNorm = normalizeResource(targetResource, normalizeContext);

    // Skip secret named-value .value
    if (skipSecretValues && isSecretNamedValue(sourceResource)) {
      if (
        sourceNorm.properties &&
        typeof sourceNorm.properties === 'object'
      ) {
        const props = sourceNorm.properties as Record<string, unknown>;
        delete props.value;
      }
      if (
        targetNorm.properties &&
        typeof targetNorm.properties === 'object'
      ) {
        const props = targetNorm.properties as Record<string, unknown>;
        delete props.value;
      }
    }

    // Skip Event Hub/App Insights logger credentials
    if (skipLoggerCreds && isLoggerWithCredentials(sourceResource)) {
      if (
        sourceNorm.properties &&
        typeof sourceNorm.properties === 'object'
      ) {
        const props = sourceNorm.properties as Record<string, unknown>;
        delete props.credentials;
      }
      if (
        targetNorm.properties &&
        typeof targetNorm.properties === 'object'
      ) {
        const props = targetNorm.properties as Record<string, unknown>;
        delete props.credentials;
      }
    }

    const diffs = compareNormalizedResources(sourceNorm, targetNorm);
    if (diffs.length > 0) {
      differences.push({
        resourceType: typeLabel,
        resourceName: name,
        diffType: 'property-diff',
        diffs,
      });
    }
  }

  return differences;
}

/**
 * Builds a resource map keyed by name, handling auto-generated IDs
 */
function buildResourceMap(
  resources: Array<Record<string, unknown>>,
  normalizeContext: NormalizeContext,
  excludeNames?: Set<string>,
): Map<string, Record<string, unknown>> {
  const map = new Map<string, Record<string, unknown>>();
  const autoIdItems: Array<{
    resource: Record<string, unknown>;
    normalizedJson: string;
  }> = [];

  for (const resource of resources) {
    const id = resource.id;
    if (typeof id !== 'string') continue;
    const name = extractResourceName(id);
    if (excludeNames?.has(name)) continue;

    // Auto-generated IDs: key by normalized content
    if (isAutoGeneratedName(name)) {
      const normalized = normalizeResource(resource, normalizeContext);
      const normalizedJson = JSON.stringify(normalized);
      autoIdItems.push({ resource, normalizedJson });
    } else {
      map.set(name, resource);
    }
  }

  // Sort auto-ID items by normalized JSON and assign positional keys
  autoIdItems.sort((a, b) => a.normalizedJson.localeCompare(b.normalizedJson));
  autoIdItems.forEach((item, index) => {
    map.set(`{{auto-id-${index}}}`, item.resource);
  });

  return map;
}

/**
 * Safely lists resources, returning empty array on error
 */
async function safeListResources(
  client: IApimClient,
  resourceType: ResourceType,
  context: ApimServiceContext,
  parent?: ResourceDescriptor,
): Promise<Array<Record<string, unknown>>> {
  try {
    const resources: Array<Record<string, unknown>> = [];
    const iterable = client.listResources(context, resourceType, parent);
    for await (const resource of iterable) {
      resources.push(resource);
    }
    return resources;
  } catch (error) {
    logger.debug(
      `Failed to list ${resourceType}: ${error instanceof Error ? error.message : String(error)}`,
    );
    return [];
  }
}

/**
 * Extracts resource name from ARM resource ID
 */
function extractResourceName(resourceId: string): string {
  const parts = resourceId.split('/');
  return parts[parts.length - 1] || '';
}

/**
 * Compares local artifact directories
 */
export async function compareLocalArtifacts(
  artifactStore: IArtifactStore,
  sourceDir: string,
  targetDir: string,
  overrides?: OverrideConfig,
  _format: 'text' | 'json' | 'table' = 'text',
  _logLevel?: LogLevel,
): Promise<CompareResult> {
  logger.info(`Comparing local artifacts: ${sourceDir} → ${targetDir}`);

  // Load artifacts from both directories
  const sourceArtifacts = await loadLocalArtifacts(
    artifactStore,
    sourceDir,
    overrides,
  );
  const targetArtifacts = await loadLocalArtifacts(
    artifactStore,
    targetDir,
    undefined, // No overrides for target
  );

  // Create a normalized context with placeholder values
  // For local compare, we use placeholders since there are no actual subscriptions/RGs
  const normalizeContext: NormalizeContext = {
    sourceServiceName: 'source',
    targetServiceName: 'target',
    sourceSubscriptionId: '00000000-0000-0000-0000-000000000000',
    targetSubscriptionId: '00000000-0000-0000-0000-000000000000',
    sourceResourceGroup: 'source-rg',
    targetResourceGroup: 'target-rg',
  };

  const differences: ComparisonDifference[] = [];
  let totalTypes = 0;
  let totalResources = 0;

  // Get all unique resource types from both sources
  const allTypes = new Set<string>();
  sourceArtifacts.forEach((_, type) => allTypes.add(type));
  targetArtifacts.forEach((_, type) => allTypes.add(type));

  // Compare each resource type
  for (const resourceType of allTypes) {
    const sourceResources = sourceArtifacts.get(resourceType) ?? [];
    const targetResources = targetArtifacts.get(resourceType) ?? [];

    // Determine skip flags based on resource type (string comparison)
    const skipSecretValues = resourceType === String(ResourceType.NamedValue);
    const skipLoggerCreds = resourceType === String(ResourceType.Logger);

    const typeDiffs = compareResourceLists(
      resourceType,
      sourceResources,
      targetResources,
      normalizeContext,
      undefined, // No exclusions for local compare
      skipSecretValues,
      skipLoggerCreds,
    );

    differences.push(...typeDiffs);
    totalTypes++;
    totalResources += Math.max(sourceResources.length, targetResources.length);
  }

  const totalDifferences = differences.filter(
    (d) => d.diffType !== 'property-diff' || (d.diffs && d.diffs.length > 0),
  ).length;

  return {
    totalTypes,
    totalResources,
    totalDifferences,
    differences,
  };
}
