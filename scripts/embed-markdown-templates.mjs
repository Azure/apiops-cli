import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';

const repoRoot = resolve(import.meta.dirname, '..');
const outputFile = resolve(repoRoot, 'src/templates/generated/embedded-markdown.ts');

const templates = [
  {
    exportName: 'copilotGitHubActionsIdentitySetupPromptTemplate',
    path: 'src/templates/copilot/identity-setup-prompt-github-actions.md',
  },
  {
    exportName: 'copilotAzureDevOpsIdentitySetupPromptTemplate',
    path: 'src/templates/copilot/identity-setup-prompt-azure-devops.md',
  },
  {
    exportName: 'copilotConfigureFilterPromptTemplate',
    path: 'src/templates/copilot/configure-filter-prompt.md',
  },
  {
    exportName: 'copilotConfigureOverridesPromptTemplate',
    path: 'src/templates/copilot/configure-overrides-prompt.md',
  },
  {
    exportName: 'azureDevOpsIdentitySetupCoreTemplate',
    path: 'src/templates/shared/identity-setup-azure-devops-core.md',
  },
  {
    exportName: 'githubActionsIdentityGuideTemplate',
    path: 'src/templates/identity/identity-guide-github-actions.md',
  },
  {
    exportName: 'azureDevOpsIdentityGuideTemplate',
    path: 'src/templates/identity/identity-guide-azure-devops.md',
  },
  {
    exportName: 'copilotGithubEnvironmentFederatedCredentialTemplate',
    path: 'src/templates/shared/github-environment-federated-credential.md',
  },
  {
    exportName: 'copilotGithubEnvironmentSecretCommandsTemplate',
    path: 'src/templates/shared/github-environment-secret-commands.md',
  },
];

const rendered = await Promise.all(
  templates.map(async ({ exportName, path }) => {
    const absolutePath = resolve(repoRoot, path);
    const content = await readFile(absolutePath, 'utf8');
    return `export const ${exportName} = ${JSON.stringify(content)} as const;`;
  })
);

const source = `// Copyright (c) Microsoft Corporation.\n// Licensed under the MIT license.\n/**\n * AUTO-GENERATED FILE.\n * Generated by scripts/embed-markdown-templates.mjs\n */\n\n${rendered.join('\n\n')}\n`;

await mkdir(dirname(outputFile), { recursive: true });
await writeFile(outputFile, source);
